print("\n*************************************indexing*************************************")
t = (1, "20210101", "비트코인", 19638000)

print(t)            # 튜플 t 전체 출력
# 출력: (1, '20210101', '비트코인', 19638000)

print(t[2])         # index가 2인 원소 출력
print(t[-1])        # index가 뒤에서 첫번째인 원소 출력
# 출력
# 비트코인
# 19638000


print("\n*************************************추가*************************************")
t = (1, "20210101", "비트코인")

t = t + (19638000, 324)
print(t)
# 출력: (1, '20210101', '비트코인', 19638000, 324)

t = (1, "20210101", "비트코인")
t = t * 2           # tuple 안의 원소를 2번 반복
print(t)
# 출력: (1, '20210101', '비트코인', 1, '20210101', '비트코인')

print("\n*************************************tuple*************************************")
i = (2021)
print(type(i))
# 출력: <class 'int'>

t = (2021,)
print(type(t))
print(len(t))
# 출력:
# <class 'tuple'>
# 1
print("\n*************************************slicing*************************************")
t = (1, "20210101", "비트코인", 19638000)

print(t[1:3])       # index가 1인 원소부터 3 이전의 원소까지 출력
# 출력: ('20210101', '비트코인')